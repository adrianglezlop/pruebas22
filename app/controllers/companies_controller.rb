class CompaniesController < ApplicationController
  before_action :set_company, only: [:show, :edit, :update, :destroy]

  # GET /companies
  # GET /companies.json
  def index
    suc = params[:sucursal].to_i
    @companies = Company.all.order(:nombre_de_empresa)
    @companies = @companies.where(branch_office_id:suc) unless params[:sucursal].nil? or params[:sucursal] == ""
  end

  # GET /companies/1
  # GET /companies/1.json
  def show
  end

  # GET /companies/new
  def new
    @company = Company.new
  end

  # GET /companies/1/edit
  def edit
  end

  # POST /companies
  # POST /companies.json
  def create
    @company = Company.new(company_params)

    respond_to do |format|
      if @company.save
        format.html { redirect_to @company, notice: 'Company was successfully created.' }
        format.json { render :show, status: :created, location: @company }
      else
        format.html { render :new }
        format.json { render json: @company.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /companies/1
  # PATCH/PUT /companies/1.json
  def update
    respond_to do |format|
      if @company.update(company_params)
        format.html { redirect_to @company, notice: 'Company was successfully updated.' }
        format.json { render :show, status: :ok, location: @company }
      else
        format.html { render :edit }
        format.json { render json: @company.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /companies/1
  # DELETE /companies/1.json
  def destroy
    @company.destroy
    respond_to do |format|
      format.html { redirect_to companies_url, notice: 'Company was successfully destroyed.' }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_company
      @company = Company.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def company_params
      params.require(:company).permit(:branch_office_id,:clave, :nombre_de_empresa, :razon_social, :calle, :numero_exterior, :numero_interior, :colonia, :codigo_postal, :municipio, :estado, :apellido_paterno, :apellido_materno, :nombre_1, :nombre_2, :telefono_de_oficina, :telefono_celular, :correo_electronico_1, :correo_electronico_2, :cuenta_bacaria, :banco, :comicion_por_cobranza, :comicion_por_colocacion, :monto_maximo_de_prestamo, :permitir_prestamo_multiple)
    end
end
